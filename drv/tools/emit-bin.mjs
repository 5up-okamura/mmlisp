// Assemble the driver and emit the artifacts an SGDK (or any 68k) project
// links against:
//   drv/sgdk/mmlispdrv.bin       raw Z80 image (for an SGDK BIN resource)
//   drv/sgdk/mmlispdrv_bin.h     the same image as a C array + size macro
//
// The image is position-independent data (uploaded to Z80 RAM at 0x0000), so
// both forms are interchangeable; the .bin suits `BIN` resources, the header
// suits a direct `Z80_loadCustomDriver(mmlispdrv_bin, MMLISPDRV_SIZE)`.
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { buildDriver } from "./build-driver.mjs";

const here = dirname(fileURLToPath(import.meta.url));
const outDir = join(here, "..", "sgdk");

const { resident: bytes, overlay } = buildDriver();

function cArray(blob, sym, guard, note) {
  const rows = [];
  for (let i = 0; i < blob.length; i += 12) {
    rows.push(
      "    " +
        [...blob.slice(i, i + 12)].map((b) => `0x${b.toString(16).padStart(2, "0")}`).join(", ") +
        ",",
    );
  }
  return `// Generated by drv/tools/emit-bin.mjs — DO NOT EDIT BY HAND.
// ${note}
#ifndef ${guard}
#define ${guard}

#include <types.h>

#define ${guard.replace("_H", "")}_SIZE ${blob.length}

const u8 ${sym}[${blob.length}] = {
${rows.join("\n")}
};

#endif
`;
}

// Resident image (uploaded to Z80 RAM at 0x0000).
writeFileSync(join(outDir, "mmlispdrv.bin"), bytes);
writeFileSync(
  join(outDir, "mmlispdrv_bin.h"),
  cArray(bytes, "mmlispdrv_bin", "MMLISPDRV_BIN_H",
    `MMLispDRV resident Z80 image, ${bytes.length} bytes, uploaded to Z80 RAM at 0x0000.`),
);

// Overlay ROM blob (cold code, driver.md §5): placed in ROM 32 KB-aligned, its
// bank passed to MMLisp_init; the Z80 loads pieces on demand into OVERLAY_SLOT.
let msg = `sgdk/mmlispdrv.bin + mmlispdrv_bin.h: ${bytes.length} bytes`;
if (overlay.length) {
  writeFileSync(join(outDir, "mmlispdrv_ovl.bin"), overlay);
  writeFileSync(
    join(outDir, "mmlispdrv_ovl_bin.h"),
    cArray(overlay, "mmlispdrv_ovl_bin", "MMLISPDRV_OVL_BIN_H",
      `MMLispDRV overlay ROM blob, ${overlay.length} bytes, 32 KB-aligned in ROM.`),
  );
  msg += `; overlay ${overlay.length} bytes`;
}
console.log(msg);
